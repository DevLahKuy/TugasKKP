package com.inventory.model;



public class ModelSuppliers {

    public int getIdSuppliers() {
        return idSuppliers;
    }

    public void setIdSuppliers(int idSuppliers) {
        this.idSuppliers = idSuppliers;
    }

    public String getNameSuppliers() {
        return nameSuppliers;
    }

    public void setNameSuppliers(String nameSuppliers) {
        this.nameSuppliers = nameSuppliers;
    }

    public String getAddressSuppliers() {
        return addressSuppliers;
    }

    public void setAddressSuppliers(String addressSuppliers) {
        this.addressSuppliers = addressSuppliers;
    }

    public String getPhoneSuppliers() {
        return phoneSuppliers;
    }

    public void setPhoneSuppliers(String phoneSuppliers) {
        this.phoneSuppliers = phoneSuppliers;
    }

    public String getEmailSuppliers() {
        return emailSuppliers;
    }

    public void setEmailSuppliers(String emailSuppliers) {
        this.emailSuppliers = emailSuppliers;
    }
  
    private int idSuppliers;
    private String nameSuppliers;
    private String addressSuppliers;
    private String phoneSuppliers;
    private String emailSuppliers;
}
